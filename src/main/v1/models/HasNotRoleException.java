package models;

public class HasNotRoleException extends Exception {
	public HasNotRoleException() {
		super();
	}

	public HasNotRoleException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public HasNotRoleException(String arg0) {
		super(arg0);
	}

	public HasNotRoleException(Throwable arg0) {
		super(arg0);
	}
}
